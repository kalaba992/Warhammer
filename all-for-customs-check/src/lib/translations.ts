export function convertScript(text: string, targetScript: 'latin' | 'cyrillic'): string {
  if (targetScript === 'cyrillic') {
    let result = text
    const sortedKeys = Object.keys(latinToCyrillicMap).sort((a, b) => b.length - a.length)
    for (const latin of sortedKeys) {
      const cyrillic = latinToCyrillicMap[latin]
      result = result.split(latin).join(cyrillic)
    }
    return result
  } else {
    let result = text
    const sortedKeys = Object.keys(cyrillicToLatinMap).sort((a, b) => b.length - a.length)
    for (const cyrillic of sortedKeys) {
      const latin = cyrillicToLatinMap[cyrillic]
      result = result.split(cyrillic).join(latin)
    }
    return result
  }
}

export function applyScriptVariant(
  text: string, 
  language: Language, 
  scriptVariant: 'latin' | 'cyrillic' = 'latin'
): string {
  const scriptsSupported = SUPPORTED_LANGUAGES.find(l => l.code === language)?.supportsScriptVariant
  
  if (!scriptsSupported) {
    return text
  }
  
  return convertScript(text, scriptVariant)
}
